// Define variables and initial functions 
class Car {
    constructor(color, convertible) {
    this.color = color
    this.speed = 0
    this.convertible = convertible
    this.isConvertible = false
    }

    accelerate(number) {
        this.speed += number
    }

    decelerate(number) {
        this.speed -= number
    }

    if(convertible = true) {
        isConvertible = true
    }
}

module.exports = Car



